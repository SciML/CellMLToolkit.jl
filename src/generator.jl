function derivate(op::Symbolic, v::Symbolic)
    n = length(arguments(op))

    iseq = isequal(op, v)
    if op isa Sym && iseq
        return 1
    elseif n == 0
        if iseq
            return 1
        else
            return 0
        end
    elseif n == 1
        a = derivate(arguments(op)[1], v)
        Î´a = ModelingToolkit.derivative(op, 1)
        return a * Î´a
    elseif n >= 2
        l = []
        for i = 1:n
            push!(l, derivate(arguments(op)[i], v) * ModelingToolkit.derivative(op, i))
        end
        return Term{Real}(+, l)
    end
end

