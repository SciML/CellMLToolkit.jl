import Base.isconst

isconst(x::Number) = true
isconst(x::Symbolic) = false
isminusone(x::Symbolic) = false
isminusone(x) = isone(-x)

simplify(x::Equation) = x.lhs ~ simplify(x.rhs)

function simplify(op::Symbolic)
    n = length(arguments(op))

    if n == 0
        return op
    elseif n == 1
        return simplify_unary(op)
    elseif n == 2
        return simplify_binary(op)
    else
        return simplify_nary(op)
    end
end

function simplify_unary(op::Symbolic)
    return Term{Real}(operation(op), [simplify(arguments(op)[1])])
end

function simplify_binary(op::Symbolic)
    g = operation(op)
    x = simplify(arguments(op)[1])
    y = simplify(arguments(op)[2])

    if g == +
        if iszero(x)
            return y
        elseif iszero(y)
            return x
        elseif isconst(x) && isconst(y)
            return x.value + y.value
        else
            return x + y
        end
    elseif g == -
        if iszero(x)
            return -y
        elseif iszero(y)
            return x
        elseif isconst(x) && isconst(y)
            return x.value - y.value
        else
            return x - y
        end
    elseif g == *
        if iszero(x)
            return x
        elseif iszero(y)
            return y
        elseif isone(x)
            return y
        elseif isone(y)
            return x
        elseif isminusone(x)
            return -y
        elseif isminusone(y)
            return -x
        elseif isconst(x) && isconst(y)
            return x.value * y.value
        elseif isconst(x)
            # we reverse the order of operands here to suppress implicit multiplication
            # as it can cause ambiguity if a number literal is followed by a variable, whose
            # name starts with E, e, or f and the second letter is a digit
            return y * x
        else
            return x * y
        end
    elseif g == /
        if iszero(x)
            return x
        elseif iszero(y)
            error("divide by zero")
        elseif isone(y)
            return x
        elseif isminusone(y)
            return -x
        elseif isconst(x) && isconst(y)
            return x.value / y.value
        else
            return x / y
        end
    elseif g == ^
        if iszero(x)
            return x
        elseif iszero(y)
            return 1
        elseif isone(x)
            return x
        elseif isone(y)
            return x
        elseif isconst(x) && isconst(y)
            return x.value ^ y.value
        else
            return x ^ y
        end
    else
        Term{Real}(g, [x, y])
    end
end

function simplify_nary(op::Symbolic)
    g = operation(op)
    x = simplify(arguments(op)[1])
    y = simplify(Term{Real}(g, arguments(op)[2:end]))

    if g == +
        if iszero(x)
            return y
        else
            return x + y
        end
    elseif g == *
        if iszero(x)
            return x
        elseif isone(x)
            return y
        elseif isminusone(x)
            return -y
        else
            return x * y
        end
    else
        return Term{Real}(g, [x,y])
    end
end

